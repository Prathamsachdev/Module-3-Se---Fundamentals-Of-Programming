//Vowel or Consonant using switch case

#include<stdio.h>

int main()
{

	char chr;
	
	printf("Enter character :");
	scanf(" %c",&chr);
	
	switch(chr)
	{
		case 'a':
		{
		
			printf("character is vowel ");
			break;
		}
		case 'e':
		{
		
			printf("character is vowel ");
			break;
		}
		case 'i':
		{
			
			printf("character is vowel ");
			break;
		}
		case 'o':
		{
			
			printf("character is vowel ");
			break;
		}
		case 'u':
		{
		
			printf("character is vowel ");
			break;
		}
		default :
		{
			printf("character is constant ");
		}
	}
	
	return 0;
}
